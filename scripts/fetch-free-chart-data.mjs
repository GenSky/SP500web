import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import YahooFinance from "yahoo-finance2";

const ROOT = process.cwd();
const CONSTITUENTS_FILE = path.join(ROOT, "src", "data", "indexConstituents.ts");
const OUTPUT_FILE = path.join(ROOT, "src", "data", "freeChartData.ts");
const yahooFinance = new YahooFinance({ suppressNotices: ["yahooSurvey"] });

const args = parseArgs(process.argv.slice(2));
const limit = numberArg(args.limit, Number.POSITIVE_INFINITY);
const delayMs = numberArg(args.delay, 150);
const symbols = args.symbols ? new Set(String(args.symbols).split(",").map((symbol) => symbol.trim().toUpperCase()).filter(Boolean)) : null;
const period = String(args.period ?? "1y");
const interval = String(args.interval ?? "1wk");
const periodDays = periodToDays(period);

const period2 = new Date();
const period1 = new Date(period2);
period1.setDate(period1.getDate() - periodDays);

const constituents = readConstituents(CONSTITUENTS_FILE)
  .filter((stock) => !symbols || symbols.has(stock.ticker))
  .slice(0, limit);

if (constituents.length === 0) {
  throw new Error("No constituents matched the requested chart refresh scope.");
}

console.log(`Fetching free Yahoo Finance chart data for ${constituents.length} ticker(s)...`);
const startedAt = new Date();
const chartData = new Map();
const failures = [];

for (const [index, stock] of constituents.entries()) {
  const symbol = toYahooSymbol(stock.ticker);
  try {
    const chart = await yahooFinance.chart(symbol, { period1, period2, interval });
    const points = (chart.quotes ?? [])
      .filter((quote) => [quote.open, quote.high, quote.low, quote.close, quote.volume].every((value) => Number.isFinite(Number(value))))
      .map((quote) => [
        new Date(quote.date).toISOString().slice(0, 10),
        round(Number(quote.open)),
        round(Number(quote.high)),
        round(Number(quote.low)),
        round(Number(quote.close)),
        Math.round(Number(quote.volume))
      ]);
    if (points.length < 2) throw new Error("not enough chart points returned");
    chartData.set(stock.ticker, points);
    console.log(`${index + 1}/${constituents.length} ${stock.ticker} ok (${points.length} points)`);
  } catch (error) {
    failures.push({ ticker: stock.ticker, message: error instanceof Error ? error.message : String(error) });
    console.warn(`${index + 1}/${constituents.length} ${stock.ticker} failed: ${failures.at(-1).message}`);
  }
  if (index < constituents.length - 1 && delayMs > 0) await sleep(delayMs);
}

writeDataFile(chartData, failures, startedAt, { period, interval });
console.log(`Wrote ${chartData.size} ticker chart(s) to ${path.relative(ROOT, OUTPUT_FILE)}.`);
if (failures.length > 0) console.warn(`${failures.length} ticker(s) failed. The website will show a no-chart message for those tickers.`);

function writeDataFile(data, failures, startedAt, options) {
  const generatedAt = new Date().toISOString();
  const entries = Array.from(data.entries()).map(([ticker, points]) => `  ${q(ticker)}: [\n${points.map((point) => `    [${point.map((value) => typeof value === "string" ? q(value) : value).join(", ")}]`).join(",\n")}\n  ]`).join(",\n");
  const failureComment = failures.length > 0
    ? `// Failed tickers: ${failures.map((failure) => `${failure.ticker} (${failure.message.replace(/\s+/g, " ")})`).join("; ")}\n`
    : "";
  const content = `// Generated by scripts/fetch-free-chart-data.mjs on ${generatedAt}.\n// Free Yahoo Finance chart data is unofficial and may be delayed, incomplete, rate-limited, or unavailable.\n// Refresh started at ${startedAt.toISOString()} using period=${options.period}, interval=${options.interval}.\n${failureComment}export type ChartPoint = [date: string, open: number, high: number, low: number, close: number, volume: number];\n\nexport const freeChartData: Record<string, ChartPoint[]> = {\n${entries}\n};\n`;
  fs.writeFileSync(OUTPUT_FILE, content);
}

function readConstituents(filePath) {
  const source = fs.readFileSync(filePath, "utf8");
  const blocks = source.match(/\{\n\s+ticker:[\s\S]*?\n\s+\}/g) ?? [];
  return blocks.map((block) => ({ ticker: capture(block, /ticker: "([^"]+)"/) })).filter((stock) => stock.ticker);
}

function parseArgs(argv) {
  const parsed = {};
  const positional = argv.filter((arg) => !String(arg).startsWith("--"));
  if (positional[0] && parsed.limit === undefined) parsed.limit = positional[0];
  if (positional[1] && parsed.delay === undefined) parsed.delay = positional[1];
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (!arg.startsWith("--")) continue;
    const [key, inlineValue] = arg.slice(2).split("=", 2);
    parsed[key] = inlineValue ?? argv[index + 1] ?? true;
    if (inlineValue === undefined && argv[index + 1] && !argv[index + 1].startsWith("--")) index += 1;
  }
  return parsed;
}

function periodToDays(periodValue) {
  const match = periodValue.match(/^(\d+)(d|mo|y)$/);
  if (!match) return 370;
  const amount = Number(match[1]);
  const unit = match[2];
  if (unit === "d") return amount;
  if (unit === "mo") return amount * 31;
  return amount * 370;
}

function numberArg(value, fallback) {
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : fallback;
}

function capture(textValue, regex) {
  return textValue.match(regex)?.[1] ?? "";
}

function toYahooSymbol(ticker) {
  return ticker.replace(".", "-");
}

function q(value) {
  return JSON.stringify(value);
}

function round(value) {
  return Math.round(value * 100) / 100;
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}
